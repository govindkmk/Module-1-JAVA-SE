class EmpHireDate{
	private String month;
	private int day;
	private int year;
	
	public void setMonth(String month){
		this.month = month;
	}
	public String getMonth(){
		return this.month;
	}
	
	public void setDay(int day){
		this.day = day;
	}
	public int getDay(){
		return this.day;
	}
	
	public void setYear(int year){
		this.year = year;
	}
	public int getYear(){
		return this.year;
	}
	
	

}